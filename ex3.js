// CONCEPT REVIEW: Using the ternary operator
// 1. Create a function that's:
// a) called favouriteAnimal
// b) takes one parameter, an animal
// c) returns "that's my favourite animal too!" if the animal is dog and if not returns "i don't like that animal"
   // the return statement should use a ternary operator, not an if else statement

function favouriteAnimal(animal) {
return animal = "dog" ? "that's my favourite too!" : "that's not my favourite";
}